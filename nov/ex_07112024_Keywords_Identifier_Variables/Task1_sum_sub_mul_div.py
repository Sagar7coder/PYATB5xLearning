# Task 1
# Take a 3 input from the user
# perform the sum, sub, mul and div

# taking user input
num1 = int(input("Enter number 1 : "))
num2 = int(input("Enter number 2 : "))
num3 = int(input("Enter number 3 : "))

# Calculation
sum = num1 + num2 + num3
sub = num1 - num2 - num3
mul = num1 * num2 * num3
div = num1 / num2 / num3

# printing output
print("Addition of three numbers", sum)
print("Subtraction of three number", sub)
print("Multiplication of three number", mul)
print("Division of three numbers", div)
